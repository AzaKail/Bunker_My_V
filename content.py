def generate_player_card() -> dict:
    return {
        "gender":          random.choice(GENDERS),
        "build":           random.choice(BUILDS),
        "human_trait":     random.choice(HUMAN_TRAITS),
        "profession":      random.choice(PROFESSIONS),
        "health":          random.choice(HEALTH),
        "hobby":           random.choice(HOBBIES),
        "phobia":          random.choice(PHOBIAS),
        "large_inventory": random.choice(LARGE_INVENTORY),
        "backpack":        random.choice(BACKPACKS),
        "additional_fact": random.choice(ADDITIONAL_FACTS),
        "special_ability": random.choice(SPECIAL_ABILITIES),
    }


def generate_scenario(player_count: int) -> dict:
    scenario = random.choice(BUNKER_SCENARIOS).copy()
    capacity = max(2, int(player_count * scenario["capacity_ratio"]))
    scenario["capacity"] = capacity
    scenario["bunker"] = scenario["bunker"].format(capacity=capacity)
    return scenario
