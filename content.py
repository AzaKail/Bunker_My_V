def generate_scenario(player_count: int) -> dict:
    scenario = random.choice(BUNKER_SCENARIOS).copy()
    capacity = max(2, int(player_count * scenario["capacity_ratio"]))
    scenario["capacity"] = capacity
    scenario["bunker"] = scenario["bunker"].format(capacity=capacity)
    return scenario


# ── Автогенерация static/descriptions.js ───────────────────────────────────

def build_descriptions_js() -> str:
    """Собирает JS-файл из описаний, встроенных в пулы PROFESSIONS и SPECIAL_ABILITIES."""
    all_desc: dict[str, str] = {}
    for pool in [PROFESSIONS, SPECIAL_ABILITIES]:
        for entry in pool:
            if isinstance(entry, dict) and entry.get("d"):
                all_desc[entry["v"]] = entry["d"]
    lines = [
        "// AUTO-GENERATED from content.py — не редактировать вручную",
        "// Чтобы добавить описание — правь content.py, перезапусти сервер",
        "",
        "const ALL_DESCRIPTIONS = " + json.dumps(all_desc, ensure_ascii=False, indent=2) + ";",
        "",
        "const DESCRIBED_TRAITS = ['profession', 'special_ability'];",
    ]
    return "\n".join(lines)


def write_descriptions_js(path: str = "static/descriptions.js") -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write(build_descriptions_js())
    print(f"[content] descriptions.js written → {path}")
